package calendiary.vo;

public class PuMemo {
	private String puMemo;
	private String wTime;
	private String name;
	private String day;
	private String id;
	
	public String getPuMemo()
	{
		return puMemo;
	}
	public void setPuMemo(String puMemo)
	{
		this.puMemo = puMemo;
	}
	public String getWTime()
	{
		return wTime;
	}
	public void setWTime(String wTime)
	{
		this.wTime = wTime;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getDay()
	{
		return day;
	}
	public void setDay(String day)
	{
		this.day = day;
	}
	public String getId()
	{
		return id;
	}
	public void setId(String id)
	{
		this.id = id;
	}
}
